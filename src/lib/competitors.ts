const raceMeeResultsMapper = (raw: RaceMeetResultRaw[]): RaceMeetResult[] => {
	return raw.map((r) => ({
		...r,
		// time: parseFloat(r.time),
		date: new Date(r.date),
		gender: r.gender as 'Male' | 'Female'
	}));
};

